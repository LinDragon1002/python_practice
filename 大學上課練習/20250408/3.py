print(f'{compute_total(咖啡X=2, 拿鐵L=1):,}元') 
print(f'{compute_total(奶茶L=4, 珍奶X=3, 咖啡X=4, 拿鐵X=5):,}元') 
print(f'{compute_total(紅茶S=3, 奶茶L=2):,}元')
print(f'{compute_total(紅茶L=1, 奶茶X=1, 咖啡L=1, 拿鐵X=1):,}元')
